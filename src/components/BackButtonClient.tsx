"use client";

import { useRouter } from "next/navigation";

interface BackButtonClientProps {
  fallbackHref: string;
}

export default function BackButtonClient({ fallbackHref }: BackButtonClientProps) {
  const router = useRouter();

  function handleBack() {
    if (window.history.length > 1) {
      router.back();
    } else {
      router.push(fallbackHref);
    }
  }

  return (
    <button onClick={handleBack} className="p-2 rounded-full">
      <svg
        width="20"
        height="20"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
      >
        <path d="M19 12H5M12 19l-7-7 7-7" />
      </svg>
    </button>
  );
}
